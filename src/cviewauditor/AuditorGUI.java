    private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked
        // Variable definition
        PropertiesController propsCntr = new PropertiesController();
        
        //Load properties file
        propsCntr.loadPropertiesFile();
        
        //Put properties into screen
        serverTxt.setText(propsCntr.getProperty("server"));
        sidTxt.setText(propsCntr.getProperty("sid"));
        portTxt.setText(propsCntr.getProperty("port"));
        userTxt.setText(propsCntr.getProperty("user"));
        passTxt.setText(propsCntr.getProperty("pass"));
        columnsTxt.setText(propsCntr.getProperty("columns"));
        tableTxt.setText(propsCntr.getProperty("table"));
        whereTxt.setText(propsCntr.getProperty("where"));
    }//GEN-LAST:event_jTabbedPane1MouseClicked

    private void saveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveButtonMouseClicked
        // Variable definition
        PropertiesController propsCntrl = new PropertiesController();
        char[] pass = passTxt.getPassword();
        String passStr = "";
        
        for(char c : pass) {
            passStr += c;
        }
        String [] props = {serverTxt.getText().trim(),
                           sidTxt.getText().trim(),
                           portTxt.getText().trim(),
                           userTxt.getText().trim(),
                           passStr,
                           columnsTxt.getText().trim(),
                           tableTxt.getText().trim(),
                           whereTxt.getText().trim()};
        
        try {
            propsCntrl.loadPropertiesFile();
            propsCntrl.storeProperties(props);
        } catch(IOException ex) {
            JOptionPane.showMessageDialog(this, 
                                         "Ha ocurrido un error guardando la configuración", 
                                         "An error message", 
                                         JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(this, 
                                      "Configuración guardada", 
                                      "An info message", 
                                      JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_saveButtonMouseClicked

    public void errorsControl(boolean state, String path) {
        if(!state) {
            JOptionPane.showMessageDialog(this, "Ha ocurrido un error", 
                                         "An error message", 
                                         JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                                          "Se ha generado el informe de auditoria en: \n" + path, 
                                          "An info message", 
                                          JOptionPane.INFORMATION_MESSAGE);
        }
        
    }
