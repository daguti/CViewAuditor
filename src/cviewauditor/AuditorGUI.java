    private void browseButonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_browseButonMouseClicked
        //Variable definition
        int returnVal;
        File file = null;
         
        fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);

        returnVal = fc.showOpenDialog(AuditorGUI.this);
        if (returnVal == JFileChooser.APPROVE_OPTION) { 
           file = fc.getSelectedFile();
        }
        if(file != null && file.getPath() != null) {
            pathText.setText(file.getPath());
        }
    }//GEN-LAST:event_browseButonMouseClicked

    private void launchButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_launchButtonMouseClicked
        // TODO add your handling code here:
        String path;
        AuditController cntrl = new AuditController();
        boolean state;
        
        //Retrieve store path
        path = pathText.getText() + "/";
        
        //Launch report generation process
        state = cntrl.createAuditReport(path);
        
        errorsControl(state, path);
    }//GEN-LAST:event_launchButtonMouseClicked

